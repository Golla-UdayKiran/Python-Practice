'''33 reserved words available in Python
import is a keyword that is used for importing the modules from Python modules
keyword is a pre-defined module in Python that contains list of 33 reserved words
kwlist will prints the 33 reserved words available in Python'''
import keyword
print(keyword.kwlist)

